  END IF
  IF ( .NOT. ALLOCATED(InData%C_fx) ) THEN
    IntKiBuf( Int_Xferred ) = 0
    Int_Xferred = Int_Xferred + 1
  ELSE
    IntKiBuf( Int_Xferred ) = 1
    Int_Xferred = Int_Xferred + 1
    IntKiBuf( Int_Xferred    ) = LBOUND(InData%C_fx,1)
    IntKiBuf( Int_Xferred + 1) = UBOUND(InData%C_fx,1)
